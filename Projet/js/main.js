/*==================== MENU SHOW Y HIDDEN ====================*/
$(document).ready(function () {
/*===== MENU SHOW =====*/
        $('.nav__toggle').on("click",function(){
            $('.nav__menu').addClass('show-menu');
        })
/*===== MENU HIDDEN =====*/
        $('.nav__close').on("click",function(){
            $('.nav__menu').removeClass('show-menu');
        })
/*==================== REMOVE MENU MOBILE ====================*/
        $('.nav__link').on("click",function(){
            $('.nav__menu').removeClass('show-menu');
        })
    }
);
